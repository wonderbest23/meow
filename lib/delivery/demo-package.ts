function documentItem(
  id: string,
  title: string,
  type: string,
  markdown: string,
  stageIndex: number | null,
): DeliveryItem {
  return {
    id,
    title,
    type,
    stageIndex,
    source: stageIndex === null ? "server_package" : "approved_artifact",
    versionLabel: stageIndex === null ? "최종 검토본" : "3판 승인본",
    approvedArtifactId: stageIndex === null ? null : `demo-artifact-${stageIndex}`,
    generatedAt: "2026-07-14T09:30:00.000Z",
    markdown,
    complete: true,
  };
}

